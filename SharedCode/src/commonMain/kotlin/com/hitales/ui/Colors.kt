package com.hitales.ui

class Colors {

    companion object {

        const val CLEAR:Int = 0

        const val TRANSPARENT:Int = 0

        /**
         * 0.0 white
         */
        const val BLACK:Int = 0xff000000.toInt()

        /**
         * 0.333 white
         */
        const val DARK:Int = 0x54ffffff

        /**
         * 0.667 white
         */
        const val LIGHT:Int = 0xaaffffff.toInt()

        /**
         * 1.0 white
         */
        const val WHITE:Int = 0xffffffff.toInt()

        /**
         *  0.5 white
         */
        const val GRAY:Int = 0x7fffffff

        /**
         * 1.0, 0.0, 0.0 RGB
         */
        const val RED:Int = 0xffff0000.toInt()

        /**
         *  0.0, 1.0, 0.0 RGB
         */
        const val GREEN:Int = 0xff00ff00.toInt()

        /**
         * 0.0, 0.0, 1.0 RGB
         */
        const val BLUE:Int = 0xff0000ff.toInt()

        /**
         *  0.0, 1.0, 1.0 RGB
         */
        const val CYAN:Int = 0xff00ffff.toInt()

        /**
         * 1.0, 1.0, 0.0 RGB
         */
        const val YELLOW:Int = 0xffffff00.toInt()

        /**
         * 1.0, 0.0, 1.0 RGB
         */
        const val MAGENTA:Int = 0xffff00ff.toInt()

        /**
         * 1.0, 0.5, 0.0 RGB
         */
        const val ORANGE:Int = 0xffff7f00.toInt()

        /**
         * 0.5, 0.0, 0.5 RGB
         */
        const val PURPLE:Int = 0xff7f007f.toInt()

        /**
         * 0.6, 0.4, 0.2 RGB
         */
        const val BROWN:Int = 0xff996633.toInt()

    }
}